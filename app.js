let currentLanguage = getStoredLanguage();

function getStoredLanguage() {
  try {
    return window.localStorage?.getItem("sunshine-language") || "en";
  } catch {
    return "en";
  }
}

function setStoredLanguage(language) {
  try {
    window.localStorage?.setItem("sunshine-language", language);
  } catch {
    // Language switching still works without persistent browser storage.
  }
}

function t(key) {
  return translations[currentLanguage][key] || translations.en[key] || key;
}

function localized(value) {
  if (value && typeof value === "object") return value[currentLanguage] || value.en || "";
  if (value === "Melbourne") return t("city.melbourne");
  if (value === "Sydney") return t("city.sydney");
  return value || "";
}

function metricLabel(label) {
  const key = `metric.${label.toLowerCase()}`;
  return t(key);
}

let revealObserver;

function revealElements(elements) {
  elements.forEach((element, index) => {
    element.classList.add("reveal");
    element.style.transitionDelay = `${Math.min(index % 6, 5) * 45}ms`;

    if (revealObserver) {
      revealObserver.observe(element);
    } else {
      element.classList.add("is-visible");
    }
  });
}

function setupRevealEffects() {
  const revealTargets = document.querySelectorAll(
    ".section, .section-compact, .partnership-cta, .contact-section, .value-grid article, .mission-grid article, .capability-list article, .news-grid article"
  );

  if (!("IntersectionObserver" in window)) {
    revealElements(revealTargets);
    return;
  }

  revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      });
    },
    {
      threshold: 0.12,
      rootMargin: "0px 0px -8% 0px"
    }
  );

  revealElements(revealTargets);
}

function setupHeroBackgrounds() {
  const slides = Array.from(document.querySelectorAll(".hero-media"));
  if (slides.length <= 1 || window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  slides.forEach((slide, index) => {
    slide.classList.toggle("is-active", index === 0);
  });

  let activeIndex = 0;
  window.setInterval(() => {
    const previousIndex = activeIndex;
    activeIndex = (activeIndex + 1) % slides.length;
    slides[activeIndex].classList.add("is-active");
    window.setTimeout(() => {
      slides[previousIndex].classList.remove("is-active");
    }, 2400);
  }, 7000);
}

function applyLanguage(language) {
  currentLanguage = language;
  setStoredLanguage(language);
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.title = t("meta.title");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });
  document.querySelectorAll("[data-i18n-content]").forEach((element) => {
    element.setAttribute("content", t(element.dataset.i18nContent));
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  });

  languageToggle.textContent = language === "zh" ? "EN" : "CN";
  languageToggle.setAttribute("aria-label", language === "zh" ? "Switch to English" : "Switch to Chinese");
  formStatus.textContent = "";
  renderProjects();

  if (projectDialog.open) {
    const activeProjectId = projectDialog.dataset.projectId;
    projectDialog.close();
    openProject(activeProjectId);
  }
}

function projectCard(project) {
  return `
    <article class="project-card">
      <img src="${project.image}" alt="${localized(project.name)}">
      <div class="project-card-body">
        <h3>${localized(project.name)}</h3>
        <p>${localized(project.description)}</p>
        <button class="button primary" type="button" data-project-id="${project.id}">${t("project.learnMore")}</button>
      </div>
    </article>
  `;
}

function filteredProjects() {
  const keyword = projectSearch.value.trim().toLowerCase();
  const location = locationFilter.value;
  const type = typeFilter.value;

  return projects.filter((project) => {
    const content = [
      localized(project.name),
      project.city,
      localized(project.city),
      localized(project.location),
      project.type.en,
      localized(project.type),
      localized(project.status)
    ].join(" ").toLowerCase();
    const matchesKeyword = !keyword || content.includes(keyword);
    const matchesLocation = location === "all" || project.city === location;
    const matchesType = type === "all" || project.type.en === type;
    return matchesKeyword && matchesLocation && matchesType;
  });
}

function renderProjects() {
  const visibleProjects = filteredProjects();
  projectGrid.innerHTML = visibleProjects.length
    ? visibleProjects.map(projectCard).join("")
    : `<article class="project-card"><div class="project-card-body"><h3>${t("project.noResultsTitle")}</h3><p>${t("project.noResultsCopy")}</p></div></article>`;
  revealElements(projectGrid.querySelectorAll(".project-card"));
}

function openProject(projectId) {
  const project = projects.find((item) => item.id === projectId);
  if (!project) return;

  projectDialog.dataset.projectId = projectId;

  const metrics = Object.entries(project.metrics)
    .map(([label, value]) => `<div><span>${metricLabel(label)}</span><strong>${localized(value)}</strong></div>`)
    .join("");

  projectDialogContent.innerHTML = `
    <div class="dialog-hero">
      <img src="${project.image}" alt="${localized(project.name)}">
      <div class="dialog-hero-content">
        <p class="eyebrow">${localized(project.type)}</p>
        <h2 id="dialogTitle">${localized(project.name)}</h2>
      </div>
    </div>
    <div class="dialog-body">
      <aside class="metric-list">
        <div><span>${t("project.location")}</span><strong>${localized(project.location)}</strong></div>
        <div><span>${t("project.status")}</span><strong>${localized(project.status)}</strong></div>
        ${metrics}
      </aside>
      <div class="detail-copy">
        <section>
          <h3>${t("project.overview")}</h3>
          <p>${localized(project.description)}</p>
        </section>
        <section>
          <h3>${t("project.vision")}</h3>
          <p>${localized(project.vision)}</p>
        </section>
        <section>
          <h3>${t("project.advantages")}</h3>
          <p>${t("project.advantagesCopy")}</p>
        </section>
        <a class="button primary" href="#contact" data-close-dialog>${t("project.enquiry")}</a>
      </div>
    </div>
  `;

  projectDialog.showModal();
}

function updateActiveNav() {
  const fromTop = window.scrollY + 130;
  let active = "home";

  document.querySelectorAll("main section[id]").forEach((section) => {
    if (section.offsetTop <= fromTop) {
      active = section.id;
    }
  });

  navLinks.forEach((link) => {
    link.classList.toggle("is-active", link.getAttribute("href") === `#${active}`);
  });
}

